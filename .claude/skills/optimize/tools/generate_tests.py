def generate_cpp_benchmark(
    file_path: Path,
    functions: List[Dict[str, str]]
) -> str:
    """Generate C++ benchmark using Google Benchmark"""

    header_name = file_path.stem

    benchmark_code = f'''/**
 * Performance Benchmark for {file_path.name}
 * Generated by auto-optimizer
 *
 * This benchmark measures execution time with statistical rigor.
 * Requires: Google Benchmark (benchmark)
 * Build: g++ -std=c++17 -O2 benchmark_{header_name}.cpp -lbenchmark -lpthread
 */

#include <benchmark/benchmark.h>
#include "{header_name}.h"
#include <vector>
#include <random>
#include <algorithm>

// Helper to generate test data
std::vector<int> generate_data(size_t size, const std::string& type) {{
    std::vector<int> data(size);
    std::random_device rd;
    std::mt19937 gen(rd());

    if (type == "random") {{
        std::uniform_int_distribution<> dis(0, 10000);
        for (auto& val : data) val = dis(gen);
    }} else if (type == "sorted") {{
        std::iota(data.begin(), data.end(), 0);
    }} else if (type == "reverse") {{
        std::iota(data.rbegin(), data.rend(), 0);
    }}
    return data;
}}

'''

    # Generate benchmark functions
    for func in functions:
        func_name = func['name']
        benchmark_code += f'''
// Benchmark for {func_name}
static void BM_{func_name}_Small(benchmark::State& state) {{
    auto data = generate_data(100, "random");
    for (auto _ : state) {{
        auto copy = data;
        {func_name}(copy);
        benchmark::DoNotOptimize(copy);
    }}
}}
BENCHMARK(BM_{func_name}_Small);

static void BM_{func_name}_Medium(benchmark::State& state) {{
    auto data = generate_data(1000, "random");
    for (auto _ : state) {{
        auto copy = data;
        {func_name}(copy);
        benchmark::DoNotOptimize(copy);
    }}
}}
BENCHMARK(BM_{func_name}_Medium);

static void BM_{func_name}_Large(benchmark::State& state) {{
    auto data = generate_data(10000, "random");
    for (auto _ : state) {{
        auto copy = data;
        {func_name}(copy);
        benchmark::DoNotOptimize(copy);
    }}
}}
BENCHMARK(BM_{func_name}_Large);

'''

    benchmark_code += '''
BENCHMARK_MAIN();
'''

    return benchmark_code


def generate_tests_for_file(
    file_path: Path,
    output_dir: Path,
    force: bool = False
) -> GeneratedTests:
    """
    Generate unit tests and benchmarks for a source file.

    Args:
        file_path: Path to source file
        output_dir: Directory to write generated tests
        force: Overwrite existing tests

    Returns:
        GeneratedTests object with paths and content
    """
    language = detect_language(file_path)
    functions = get_function_signatures(file_path, language)

    # Create output directories
    unit_test_dir = output_dir / ".unit_tests"
    benchmark_dir = output_dir / ".perf_tests"
    unit_test_dir.mkdir(exist_ok=True)
    benchmark_dir.mkdir(exist_ok=True)

    # Determine file extensions based on language
    if language in ['cpp', 'c']:
        test_ext = '.cpp'
    elif language in ['javascript', 'typescript']:
        test_ext = '.test.js' if language == 'javascript' else '.test.ts'
    elif language == 'go':
        test_ext = '_test.go'
    elif language == 'rust':
        test_ext = '_test.rs'
    elif language == 'java':
        test_ext = 'Test.java'
    else:
        test_ext = '.py'

    # Generate unit tests
    if language in ['cpp', 'c']:
        unit_test_path = unit_test_dir / f"test_{file_path.stem}{test_ext}"
        unit_test_content = generate_cpp_unit_test(file_path, functions)
    elif language == 'python':
        unit_test_path = unit_test_dir / f"test_{file_path.stem}{test_ext}"
        unit_test_content = generate_python_unit_test(file_path, functions)
    else:
        unit_test_path = unit_test_dir / f"test_{file_path.stem}{test_ext}"
        unit_test_content = f"// Unit tests for {file_path.name}\n// Language: {language}\n// TODO: Generate tests for this language\n"

    if force or not unit_test_path.exists():
        unit_test_path.write_text(unit_test_content)

    # Generate benchmarks
    if language in ['cpp', 'c']:
        benchmark_path = benchmark_dir / f"benchmark_{file_path.stem}{test_ext}"
        benchmark_content = generate_cpp_benchmark(file_path, functions)
    elif language == 'python':
        benchmark_path = benchmark_dir / f"benchmark_{file_path.stem}{test_ext}"
        benchmark_content = generate_python_benchmark(file_path, functions)
    else:
        benchmark_path = benchmark_dir / f"benchmark_{file_path.stem}{test_ext}"
        benchmark_content = f"// Benchmark for {file_path.name}\n// Language: {language}\n// TODO: Generate benchmark for this language\n"

    if force or not benchmark_path.exists():
        benchmark_path.write_text(benchmark_content)

    return GeneratedTests(
        unit_test_path=unit_test_path,
        benchmark_path=benchmark_path,
        unit_test_content=unit_test_content,
        benchmark_content=benchmark_content
    )


def verify_tests_run(test_path: Path) -> tuple[bool, str]:
    """Verify that generated tests can run"""
    try:
        result = subprocess.run(
            ["python", "-m", "pytest", str(test_path), "-v", "--collect-only"],
            capture_output=True,
            text=True,
            timeout=30
        )
        if result.returncode == 0:
            return True, "Tests collected successfully"
        else:
            return False, result.stderr
    except Exception as e:
        return False, str(e)


def main():
    parser = argparse.ArgumentParser(
        description="Generate unit tests and benchmarks for source files"
    )
    parser.add_argument(
        "target",
        type=Path,
        help="Source file or directory to generate tests for"
    )
    parser.add_argument(
        "--output", "-o",
        type=Path,
        default=Path.cwd(),
        help="Output directory for generated tests"
    )
    parser.add_argument(
        "--force", "-f",
        action="store_true",
        help="Overwrite existing test files"
    )
    parser.add_argument(
        "--verify",
        action="store_true",
        help="Verify generated tests can run"
    )

    args = parser.parse_args()

    target = args.target.resolve()
    output_dir = args.output.resolve()

    if target.is_file():
        files = [target]
    elif target.is_dir():
        # Find source files of multiple languages
        files = []
        for ext in ['*.py', '*.cpp', '*.c', '*.js', '*.ts', '*.go', '*.rs', '*.java']:
            files.extend(target.glob(f'**/{ext}'))
        files = [f for f in files if not any(p.startswith('.') for p in f.parts)]
    else:
        print(f"Error: {target} does not exist")
        return 1

    print(f"Generating tests for {len(files)} file(s)...")

    for file_path in files:
        print(f"\nProcessing: {file_path}")

        generated = generate_tests_for_file(file_path, output_dir, args.force)

        print(f"  Unit tests: {generated.unit_test_path}")
        print(f"  Benchmark: {generated.benchmark_path}")

        if args.verify:
            success, msg = verify_tests_run(generated.unit_test_path)
            print(f"  Verification: {'✓' if success else '✗'} {msg}")

    print("\nDone!")
    return 0


if __name__ == "__main__":
    exit(main())
