def generate_tests_for_file(
    file_path: Path,
    output_dir: Path,
    force: bool = False
) -> GeneratedTests:
    """
    Generate unit tests and benchmarks for a source file.

    Args:
        file_path: Path to source file
        output_dir: Directory to write generated tests
        force: Overwrite existing tests

    Returns:
        GeneratedTests object with paths and content
    """
    language = detect_language(file_path)
    functions = get_function_signatures(file_path, language)

    # Create output directories
    unit_test_dir = output_dir / ".unit_tests"
    benchmark_dir = output_dir / ".perf_tests"
    unit_test_dir.mkdir(exist_ok=True)
    benchmark_dir.mkdir(exist_ok=True)

    # Generate unit tests
    unit_test_path = unit_test_dir / f"test_{file_path.stem}.py"
    if language == 'python':
        unit_test_content = generate_python_unit_test(file_path, functions)
    else:
        unit_test_content = f"# Unit tests for {file_path.name}\n# Language: {language}\n# TODO: Generate tests for this language\n"

    if force or not unit_test_path.exists():
        unit_test_path.write_text(unit_test_content)

    # Generate benchmarks
    benchmark_path = benchmark_dir / f"benchmark_{file_path.stem}.py"
    if language == 'python':
        benchmark_content = generate_python_benchmark(file_path, functions)
    else:
        benchmark_content = f"# Benchmark for {file_path.name}\n# Language: {language}\n# TODO: Generate benchmark for this language\n"

    if force or not benchmark_path.exists():
        benchmark_path.write_text(benchmark_content)

    return GeneratedTests(
        unit_test_path=unit_test_path,
        benchmark_path=benchmark_path,
        unit_test_content=unit_test_content,
        benchmark_content=benchmark_content
    )


def verify_tests_run(test_path: Path) -> tuple[bool, str]:
    """Verify that generated tests can run"""
    try:
        result = subprocess.run(
            ["python", "-m", "pytest", str(test_path), "-v", "--collect-only"],
            capture_output=True,
            text=True,
            timeout=30
        )
        if result.returncode == 0:
            return True, "Tests collected successfully"
        else:
            return False, result.stderr
    except Exception as e:
        return False, str(e)


def main():
    parser = argparse.ArgumentParser(
        description="Generate unit tests and benchmarks for source files"
    )
    parser.add_argument(
        "target",
        type=Path,
        help="Source file or directory to generate tests for"
    )
    parser.add_argument(
        "--output", "-o",
        type=Path,
        default=Path.cwd(),
        help="Output directory for generated tests"
    )
    parser.add_argument(
        "--force", "-f",
        action="store_true",
        help="Overwrite existing test files"
    )
    parser.add_argument(
        "--verify",
        action="store_true",
        help="Verify generated tests can run"
    )

    args = parser.parse_args()

    target = args.target.resolve()
    output_dir = args.output.resolve()

    if target.is_file():
        files = [target]
    elif target.is_dir():
        # Find source files
        files = list(target.glob("**/*.py"))
        files = [f for f in files if not any(p.startswith('.') for p in f.parts)]
    else:
        print(f"Error: {target} does not exist")
        return 1

    print(f"Generating tests for {len(files)} file(s)...")

    for file_path in files:
        print(f"\nProcessing: {file_path}")

        generated = generate_tests_for_file(file_path, output_dir, args.force)

        print(f"  Unit tests: {generated.unit_test_path}")
        print(f"  Benchmark: {generated.benchmark_path}")

        if args.verify:
            success, msg = verify_tests_run(generated.unit_test_path)
            print(f"  Verification: {'✓' if success else '✗'} {msg}")

    print("\nDone!")
    return 0


if __name__ == "__main__":
    exit(main())
