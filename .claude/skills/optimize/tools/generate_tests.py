def detect_language(file_path: Path) -> str:
    """Detect programming language from file extension"""
    ext_to_lang = {
        '.py': 'python',
        '.js': 'javascript',
        '.ts': 'typescript',
        '.go': 'go',
        '.rs': 'rust',
        '.java': 'java',
        '.cpp': 'cpp',
        '.c': 'c',
        '.rb': 'ruby',
        '.php': 'php',
    }
    return ext_to_lang.get(file_path.suffix.lower(), 'unknown')


def get_function_signatures(file_path: Path, language: str) -> List[Dict[str, str]]:
    """Extract function signatures from source file"""
    import re
    signatures = []

    content = file_path.read_text()

    if language == 'python':
        # Match function definitions
        pattern = r'def\s+(\w+)\s*\(([^)]*)\)'
        for match in re.finditer(pattern, content):
            signatures.append({
                'name': match.group(1),
                'params': match.group(2),
                'full': match.group(0)
            })

    elif language in ['cpp', 'c']:
        # Match C/C++ function definitions
        # Pattern: return_type function_name(params)
        # Handles both declarations and definitions
        pattern = r'(?:^|\n)\s*(?:inline\s+)?(?:static\s+)?(?:const\s+)?[\w:*&<>]+\s+(\w+)\s*\(([^)]*)\)\s*(?:const\s*)?(?:override\s*)?(?:final\s*)?[{;]'
        for match in re.finditer(pattern, content, re.MULTILINE):
            func_name = match.group(1)
            # Skip main and common keywords
            if func_name not in ['main', 'if', 'while', 'for', 'switch', 'catch']:
                signatures.append({
                    'name': func_name,
                    'params': match.group(2),
                    'full': match.group(0)
                })

    elif language in ['javascript', 'typescript']:
        # Match function definitions and arrow functions
        pattern = r'(?:function\s+(\w+)|(?:const|let|var)\s+(\w+)\s*=\s*(?:async\s*)?\([^)]*\)\s*=>)'
        for match in re.finditer(pattern, content):
            func_name = match.group(1) or match.group(2)
            signatures.append({
                'name': func_name,
                'params': '',
                'full': match.group(0)
            })

    elif language == 'go':
        # Match Go function definitions
        pattern = r'func\s+(?:\([^)]+\)\s+)?(\w+)\s*\(([^)]*)\)'
        for match in re.finditer(pattern, content):
            signatures.append({
                'name': match.group(1),
                'params': match.group(2),
                'full': match.group(0)
            })

    elif language == 'rust':
        # Match Rust function definitions
        pattern = r'(?:pub\s+)?fn\s+(\w+)\s*(?:<[^>]+>)?\s*\(([^)]*)\)'
        for match in re.finditer(pattern, content):
            signatures.append({
                'name': match.group(1),
                'params': match.group(2),
                'full': match.group(0)
            })

    elif language == 'java':
        # Match Java method definitions
        pattern = r'(?:public|private|protected)?\s*(?:static\s+)?(?:final\s+)?[\w<>[\],\s]+\s+(\w+)\s*\(([^)]*)\)'
        for match in re.finditer(pattern, content):
            func_name = match.group(1)
            if func_name not in ['if', 'while', 'for', 'switch', 'catch']:
                signatures.append({
                    'name': func_name,
                    'params': match.group(2),
                    'full': match.group(0)
                })

    return signatures


