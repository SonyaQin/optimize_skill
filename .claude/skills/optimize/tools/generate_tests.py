def detect_language(file_path: Path) -> str:
    """Detect programming language from file extension"""
    ext_to_lang = {
        '.py': 'python',
        '.js': 'javascript',
        '.ts': 'typescript',
        '.go': 'go',
        '.rs': 'rust',
        '.java': 'java',
        '.cpp': 'cpp',
        '.c': 'c',
        '.rb': 'ruby',
        '.php': 'php',
    }
    return ext_to_lang.get(file_path.suffix.lower(), 'unknown')


def get_function_signatures(file_path: Path, language: str) -> List[Dict[str, str]]:
    """Extract function signatures from source file"""
    # This is a simplified version - in production, use proper AST parsing
    signatures = []

    content = file_path.read_text()

    if language == 'python':
        import re
        # Match function definitions
        pattern = r'def\s+(\w+)\s*\(([^)]*)\)'
        for match in re.finditer(pattern, content):
            signatures.append({
                'name': match.group(1),
                'params': match.group(2),
                'full': match.group(0)
            })

    return signatures


