def benchmark(
    func: Callable,
    *args,
    iterations: int = 10,
    warmup: int = 3,
    **kwargs
) -> Dict[str, float]:
    """
    Run benchmark with statistical measurements.

    Returns:
        Dict with mean, stdev, min, max, median, p95, p99
    """
    # Warmup runs
    for _ in range(warmup):
        func(*args, **kwargs)

    # Actual measurements
    times = []
    for _ in range(iterations):
        start = time.perf_counter()
        func(*args, **kwargs)
        times.append(time.perf_counter() - start)

    times.sort()

    return {{
        "mean": statistics.mean(times),
        "stdev": statistics.stdev(times) if len(times) > 1 else 0,
        "min": min(times),
        "max": max(times),
        "median": statistics.median(times),
        "p95": times[int(len(times) * 0.95)] if len(times) >= 20 else times[-1],
        "p99": times[int(len(times) * 0.99)] if len(times) >= 100 else times[-1],
        "samples": len(times)
    }}


